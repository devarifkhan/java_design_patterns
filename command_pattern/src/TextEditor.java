class TextEditor {
    public void copy() {
        System.out.println("টেক্সট কপি করা হয়েছে।");
    }

    public void cut() {
        System.out.println("টেক্সট কাটা হয়েছে।");
    }

    public void paste() {
        System.out.println("টেক্সটে পেস্ট করা হয়েছে।");
    }

    public void undoCopy() {
        System.out.println("কপি বাতিল করা হয়েছে।");
    }

    public void undoCut() {
        System.out.println("কাটানো বাতিল করা হয়েছে।");
    }

    public void undoPaste() {
        System.out.println("পেস্ট বাতিল করা হয়েছে।");
    }
}
